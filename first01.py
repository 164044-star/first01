print("--- โปรแกรมคำนวณพื้นที่อเนกประสงค์ ---")
shape = input("เลือกรูปทรง (1.สี่เหลี่ยม 2.สามเหลี่ยม): ")

if shape == "1":
    w = float(input("ป้อนความกว้าง: "))
    l = float(input("ป้อนความยาว: "))
    if w > 0 and l > 0:
        print(f"พื้นที่รูปสี่เหลี่ยมคือ: {w * l}")
    else:
        print("ข้อผิดพลาด: ค่าต้องมากกว่า 0")
elif shape == "2":
    b = float(input("ป้อนความยาวฐาน: "))
    h = float(input("ป้อนความสูง: "))
    if b > 0 and h > 0:
        print(f"พื้นที่รูปสามเหลี่ยมคือ: {0.5 * b * h}")
    else:
        print("ข้อผิดพลาด: ค่าต้องมากกว่า 0")